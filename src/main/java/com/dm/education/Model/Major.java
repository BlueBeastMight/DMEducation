package com.dm.education.Model;

public class Major {
    private String majors_id;
    private String majors_name;
    private String department_id;

    public String getMajors_id() {
        return majors_id;
    }

    public void setMajors_id(String majors_id) {
        this.majors_id = majors_id;
    }

    public String getMajors_name() {
        return majors_name;
    }

    public void setMajors_name(String majors_name) {
        this.majors_name = majors_name;
    }

    public String getDepartment_id() {
        return department_id;
    }

    public void setDepartment_id(String department_id) {
        this.department_id = department_id;
    }
}
